public class AddFractions 
{
    public static void main(String[] args)
    {
        int numerator1 = 1;
        int denominator1 = 2;
        int numerator2 = 3;
        int denominator2 = 4;
        int sum = (numerator1*2) + numerator2;
        System.out.print("The sum of "+numerator1+"/"+ denominator1+" + "+numerator2+"/"+denominator2+" = " + sum+"/"+denominator2);
    }
}